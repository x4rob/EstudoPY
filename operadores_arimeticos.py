produto_1 = 10
produto_2 = 20
print(produto_1 + produto_2) #Soma
# lembrando quue as operaçãos são feitas da esquerda para a direita, ou seja, primeiro é feita a soma e depois a multiplicação
print(produto_1 - produto_2)
print(produto_1 * produto_2)
print(produto_1 / produto_2)
print(produto_1 // produto_2) #Divisão inteira
print(produto_1 % produto_2) #Resto da divisão
print(produto_1 ** produto_2) #Potenciação


